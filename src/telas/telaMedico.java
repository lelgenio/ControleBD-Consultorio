    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
String //Pegando os valores dos campos de texto
        nome = txtNome.getText(),
        cpf = txtCpf.getText(),
        especialidade = txtEspecialidade.getText(),
        //formatando para o m√©todo
        campos = "nome,cpf,especialidade",
        valores = nome + "','" + cpf + "','" + especialidade;

bd.adicionar("medico", campos, valores); //aplicando os valores

//limpa os campos
txtNome.setText("");
txtCpf.setText("");
txtEspecialidade.setText("");

bd.listar("medico", "nome, cpf, especialidade", tbMedico);
    }//GEN-LAST:event_btnAdicionarActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
if (cpf != "") {
    String nome = txtNome.getText(),
            especialidade = txtEspecialidade.getText(),
            //novos valores
            valores = "nome = '" + nome + "', especialidade = '" + especialidade;

    bd.editar("medico", "cpf", cpf, valores);
}
bd.listar("medico", "nome, cpf, especialidade", tbMedico);
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
if (cpf != "") {
    bd.remover("medico", "cpf", cpf);

    //limpa os campos
    txtNome.setText("");
    txtCpf.setText("");
    txtEspecialidade.setText("");
}
bd.listar("medico", "nome, cpf, especialidade", tbMedico);
    }//GEN-LAST:event_btnExcluirActionPerformed

